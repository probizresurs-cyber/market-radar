export async function POST(req: Request) {
  const access = await checkAiAccess(req);
  if (!access.allowed) return access.response;
  try {
    const { companyName, companyUrl, niche, extraContext, audienceType: audienceTypeRaw } = await req.json();

    if (!niche?.trim()) {
      return NextResponse.json({ ok: false, error: "Укажите нишу / описание продукта" }, { status: 400 });
    }

    const audienceType: TAAudienceType = audienceTypeRaw === "b2b" ? "b2b" : "b2c";

    const apiKey = process.env.OPENAI_API_KEY;
    if (!apiKey) {
      return NextResponse.json({ ok: false, error: "OpenAI API key не настроен" }, { status: 500 });
    }

    const ctrl = new AbortController();
    const timeout = setTimeout(() => ctrl.abort(), 120_000);

    const systemPrompt = audienceType === "b2b" ? SYSTEM_PROMPT_B2B : SYSTEM_PROMPT_B2C;
    const userPrompt = audienceType === "b2b"
      ? buildPromptB2B(companyName ?? "", companyUrl ?? "", niche, extraContext ?? "")
      : buildPromptB2C(companyName ?? "", companyUrl ?? "", niche, extraContext ?? "");

    let raw: string;
    try {
      const res = await fetch(`${process.env.OPENAI_BASE_URL ?? "https://api.openai.com"}/v1/chat/completions`, {
        method: "POST",
        signal: ctrl.signal,
        headers: {
          "Content-Type": "application/json",
          "Authorization": `Bearer ${apiKey}`,
        },
        body: JSON.stringify({
          model: "gpt-4o",
          messages: [
            { role: "system", content: systemPrompt },
            { role: "user", content: userPrompt },
          ],
          temperature: 0.85,
          max_tokens: 7000,
          response_format: { type: "json_object" },
        }),
      });

      if (!res.ok) {
        const errBody = await res.text();
        return NextResponse.json({ ok: false, error: `OpenAI error ${res.status}: ${errBody.slice(0, 200)}` }, { status: 500 });
      }

      const data = await res.json() as { choices: Array<{ message: { content: string } }> };
      raw = data.choices[0]?.message?.content ?? "{}";
    } finally {
      clearTimeout(timeout);
    }

    const parsed = JSON.parse(raw) as Omit<TAResult, "generatedAt" | "companyName" | "companyUrl">;

    const result: TAResult = {
      generatedAt: new Date().toISOString(),
      companyName: companyName ?? "",
      companyUrl: companyUrl ?? "",
      ...parsed,
      audienceType,
    };

    await access.log({ endpoint: "analyze-ta", model: `gpt-4o-${audienceType}` });
    return NextResponse.json({ ok: true, data: result });
  } catch (err: unknown) {
    const msg = err instanceof Error ? err.message : "Unknown error";
    return NextResponse.json({ ok: false, error: msg }, { status: 500 });
  }
}
