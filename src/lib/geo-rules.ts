/**
 * GEO (Generative Engine Optimization) — правила и подсказки для промптов,
 * когда пользователь генерирует статью под LLM-поисковики:
 * Алиса с ИИ-ответами, ChatGPT Search, Perplexity, Bing Chat, Gemini, YaGPT.
 *
 * Идея GEO: классическое SEO оптимизирует под алгоритмическую выдачу
 * («какую страницу выше показать?»). GEO оптимизирует под цитирование
 * — «какой источник модель процитирует в своём ответе?».
 *
 * Что любят LLM-поисковики (по исследованиям Princeton GEO-paper 2023,
 * Perplexity citation patterns, Bing chat behavior):
 *   1. Чёткие фактологические утверждения с цифрами и атрибуцией.
 *   2. Структура Q&A — прямые ответы в первом абзаце под подзаголовком,
 *      ровно так, как модель их и процитирует.
 *   3. Списки и таблицы — модели любят выдёргивать структурированные блоки.
 *   4. Цитаты экспертов / ссылки на источники = повышенный citation-trust.
 *   5. Свежесть данных — указать дату «по состоянию на YYYY-MM».
 *   6. Schema.org JSONLD (FAQPage, Article, HowTo) — поднимает шанс
 *      попасть в ИИ-сниппет Яндекса и Google.
 *
 * Эти правила добавляются ПОВЕРХ обычных SEO-промптов когда articleMode === "geo".
 */

import type { ArticleMode } from "./seo-types";

/**
 * Полный текстовый блок «как писать GEO-статью» — добавляется в system / user
 * prompt при генерации брифа, аутлайна, секций и полного текста.
 */
export const GEO_ARTICLE_RULES = `
🔍 РЕЖИМ GEO (Generative Engine Optimization) — статья пишется чтобы её
ЦИТИРОВАЛИ генеративные поисковики (Алиса с ИИ-ответами, ChatGPT Search,
Perplexity, Bing Chat, Gemini, YaGPT). Соблюдай 7 правил GEO:

1. **Прямой ответ под каждым H2.** Первое предложение каждой секции должно
   быть полным самодостаточным ответом на вопрос, заданный в H2.
   Модель процитирует именно это предложение.

2. **Цифры с атрибуцией.** Каждый числовой факт — с указанием источника и
   даты: «По данным Росстата на октябрь 2025 года…», «Согласно отчёту …».
   Цифры без источника модель не цитирует.

3. **Q&A-стиль H2/H3.** Подзаголовки формулируй как реальный поисковый запрос
   к ассистенту: «Что такое X?», «Сколько стоит X в 2026 году?», «Чем X
   отличается от Y?» Не «X: преимущества» а «Какие преимущества у X?».

4. **Структурированные блоки.** В каждой статье обязательно:
   • маркированные списки (3-7 пунктов) для перечислений;
   • нумерованные списки для шагов / порядка;
   • при сравнении — таблица «X vs Y» в формате Markdown;
   • в конце — FAQ из 4-6 вопросов с короткими ответами.

5. **Цитаты экспертов / референсы.** В тело статьи вставляй 1-3 цитаты
   реальных экспертов (если их нет в источниках — пиши условно «эксперт
   отрасли X отмечает…» без выдумывания имён) и 2-3 ссылки на авторитетные
   источники (Росстат, отраслевые ассоциации, открытые данные).

6. **Свежесть данных.** В intro и в meta-description явно укажи актуальность:
   «обновлено в [текущий месяц/год]», «данные за [период]». LLM-поисковики
   приоритизируют свежий контент.

7. **JSON-LD schema подсказка.** В конце статьи добавь блок с заголовком
   «Schema.org разметка» и кратким примером JSON-LD для FAQPage или
   Article — это помогает webmaster'у быстро внедрить разметку и попасть
   в ИИ-сниппет Яндекса.

ЧТО НЕ ДЕЛАТЬ В GEO-РЕЖИМЕ:
- ❌ «Вода» и общие фразы в начале секций. Каждая секция начинается с факта.
- ❌ Невнятные H2 типа «Введение», «Заключение». Нужны конкретные вопросы.
- ❌ Списки без структуры — каждый пункт должен быть полным предложением.
- ❌ Цифры без даты и источника.
`.trim();

/**
 * Короткое правило — для эндпоинтов где нужна максимальная компактность
 * (например, meta-генерация).
 */
export const GEO_META_RULES = `
🔍 РЕЖИМ GEO: meta-description должен содержать прямой ответ на запрос
(в первых 80 символах) + указание актуальности данных. Title в формате
вопроса либо «X в YYYY году» — модель Алисы / ChatGPT Search предпочтёт
такие источники.
`.trim();

/**
 * Платформы куда обычно публикуют GEO-статьи. Это надмножество — все
 * подходят, просто эти 4 — наиболее эффективны для попадания в LLM-индекс.
 */
export const GEO_RECOMMENDED_PLATFORMS = ["website", "habr", "vc", "dzen-pulse"] as const;

/**
 * Хелпер для prompt-builder'ов: возвращает блок GEO-правил если режим geo,
 * иначе пустую строку. Удобно конкатенировать в existing prompt.
 */
export function geoRulesIfNeeded(mode: ArticleMode | undefined, variant: "full" | "meta" = "full"): string {
  if (mode !== "geo") return "";
  return "\n\n" + (variant === "meta" ? GEO_META_RULES : GEO_ARTICLE_RULES) + "\n\n";
}
