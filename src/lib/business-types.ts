// ─── Business Type definitions ────────────────────────────────────────────────
// Sprint 5: 4 types that shape AI analysis prompts and scoring emphasis.

export type BusinessType = "b2c-retail" | "b2c-experience" | "b2b-services" | "b2b-saas";

export interface BusinessTypeConfig {
  id: BusinessType;
  icon: string;
  label: string;
  desc: string;
  /** Key into NICHE_COMPETITORS for competitor suggestions */
  nicheKey: "other" | "clinic" | "digital" | "b2b";
}

export const BUSINESS_TYPES: BusinessTypeConfig[] = [
  {
    id: "b2c-retail",
    icon: "🛍️",
    label: "B2C / Розница",
    desc: "Интернет-магазин, товары, маркетплейс",
    nicheKey: "other",
  },
  {
    id: "b2c-experience",
    icon: "🏥",
    label: "B2C / Сервис",
    desc: "Клиника, ресторан, салон, образование",
    nicheKey: "clinic",
  },
  {
    id: "b2b-services",
    icon: "🤝",
    label: "B2B / Услуги",
    desc: "Агентство, консалтинг, аутсорсинг",
    nicheKey: "digital",
  },
  {
    id: "b2b-saas",
    icon: "💻",
    label: "B2B / SaaS",
    desc: "Программный продукт, платформа, подписка",
    nicheKey: "b2b",
  },
];

export function getBusinessTypeConfig(type: BusinessType | undefined): BusinessTypeConfig {
  return BUSINESS_TYPES.find(t => t.id === type) ?? BUSINESS_TYPES[0];
}

/**
 * Returns a prompt addendum (in Russian) that tells Claude to evaluate
 * the website through the lens of the specific business type.
 */
export function buildBusinessTypePromptHint(type: BusinessType | undefined): string {
  switch (type) {
    case "b2c-retail":
      return `
=== ТИП БИЗНЕСА: B2C / РОЗНИЦА (интернет-магазин, товары, маркетплейс) ===
Оценивай сайт с точки зрения онлайн-продаж физическим лицам. Акцентируй внимание:
- Качество товарного каталога, фильтрации, карточек товаров (фото, описания, характеристики)
- Прозрачность цен, наличие скидок, программ лояльности, промо-акций
- Удобство корзины, оформления заказа, вариантов оплаты и доставки
- Социальные доказательства: отзывы покупателей, UGC-контент, рейтинги товаров
- Работа с возвратами, гарантиями, постпродажным сервисом
- Сигналы безопасности: сертификаты, значки оплаты, политика конфиденциальности
В recommendations и insights давай советы именно для e-commerce: конверсия корзины, средний чек, повторные покупки, LTV, работа с брошенными корзинами.
В offerAnalysis сравнивай с маркетплейсами (Ozon, Wildberries) — почему покупать именно у них.`;

    case "b2c-experience":
      return `
=== ТИП БИЗНЕСА: B2C / СЕРВИС (клиника, ресторан, салон, отель, образование) ===
Оценивай сайт с точки зрения привлечения клиентов на оффлайн-услуги или опыт. Акцентируй внимание:
- Первое впечатление: атмосфера, доверие, эмоция уже на главной странице
- Онлайн-запись / бронирование: насколько легко записаться, есть ли виджет
- Геолокация и доступность: адрес, карты, парковка, как добраться
- Визуальный контент: фото интерьера, команды, результатов работы (до/после)
- Репутация: рейтинги в 2GIS/Google/Яндекс, количество и качество отзывов
- Специалисты/команда: профили, сертификаты, опыт — людям важно знать "кто" их обслужит
- Ценообразование: прайс, пакеты, акции для первого визита
В recommendations и insights фокусируйся: конверсия в запись, удержание клиентов, средний LTV, работа с отзывами, локальное SEO.`;

    case "b2b-services":
      return `
=== ТИП БИЗНЕСА: B2B / УСЛУГИ (агентство, консалтинг, аутсорсинг) ===
Оценивай сайт с точки зрения привлечения корпоративных клиентов на услуги с длинным циклом сделки. Акцентируй внимание:
- Портфолио и кейсы: конкретные результаты с цифрами, узнаваемые клиенты
- Команда и экспертиза: профили ключевых специалистов, регалии, публикации
- Процесс работы: как устроено сотрудничество, этапы, сроки, прозрачность
- Лидогенерация: лид-магниты, аудиты, демо-звонки, чёткий CTA
- Доверие: отзывы ЛПР, юридические реквизиты, НДА, гарантии
- Отраслевая специализация: понимание индустрий клиента vs универсальный подход
В recommendations и insights фокусируйся на: лидогенерации, конверсии сайта в заявки, nurturing потенциальных клиентов, account-based маркетинге, репутации на профессиональных площадках (VC.ru, Habr, рейтинги Ruward/Tagline).`;

    case "b2b-saas":
      return `
=== ТИП БИЗНЕСА: B2B / SaaS (программный продукт, платформа, подписка) ===
Оценивай сайт с точки зрения привлечения платящих пользователей и снижения churn в продуктовом бизнесе. Акцентируй внимание:
- Ценностное предложение: понятен ли продукт за 5 секунд, есть ли hero-блок с конкретной выгодой
- Onboarding-воронка: free trial, freemium, демо — насколько легко начать пользоваться
- Ценообразование: тарифные планы, сравнение функций, прозрачность, FAQ о переходе
- Интеграции и экосистема: с какими сервисами интегрируется, API, маркетплейс приложений
- Безопасность и compliance: сертификаты (GDPR, 152-ФЗ), SLA, uptime, enterprise-доверие
- Social proof: кейсы ROI с конкретными метриками, логотипы клиентов, G2/Capterra рейтинги
- Документация и поддержка: база знаний, status page, SLA поддержки
В recommendations и insights фокусируйся на: MRR-рост, снижении churn, product-led growth, trial-to-paid конверсии, NPS, ARPU, стратегии выхода на Enterprise-сегмент.`;

    default:
      return "";
  }
}
