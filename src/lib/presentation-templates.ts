export function getTemplateById(id: string): IndustryTemplate | undefined {
  return INDUSTRY_TEMPLATES.find(t => t.id === id);
}

/** Промпт-блок для customPrompt при генерации презентации с template. */
export function buildTemplatePromptBlock(t: IndustryTemplate): string {
  return `ИНДУСТРИЯ: ${t.name}
ФОКУС: ${t.focus}

ОБЯЗАТЕЛЬНО включи следующие слайды (помимо стандартных cover/cta):
${t.mandatorySlides.map(s => `- ${s}`).join("\n")}`;
}
